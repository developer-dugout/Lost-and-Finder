package com.coding.pixel.labboapp;

public class UsersData {
    public String name, status, contact, section, institute, image, uid, time, date, postimage, description, adminname, adminstatus, adminimage, adminuid;

    public UsersData() {

    }
    public UsersData(String name, String status, String contact, String section, String institute, String image, String uid, String time, String date, String postimage, String description, String adminname, String adminstatus, String adminimage, String adminuid) {
        this.name = name;
        this.status = status;
        this.contact = contact;
        this.section = section;
        this.institute = institute;
        this.image = image;
        this.uid = uid;
        this.time = time;
        this.date = date;
        this.postimage = postimage;
        this.description = description;
        this.adminname = adminname;
        this.adminstatus = adminstatus;
        this.adminimage = adminimage;
        this.adminuid = adminuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getSection() {
        return section;
    }

    public void setSection(String section) {
        this.section = section;
    }

    public String getInstitute() {
        return institute;
    }

    public void setInstitute(String institute) {
        this.institute = institute;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPostimage() {
        return postimage;
    }

    public void setPostimage(String postimage) {
        this.postimage = postimage;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getAdminname() {
        return adminname;
    }

    public void setAdminname(String adminname) {
        this.adminname = adminname;
    }

    public String getAdminstatus() {
        return adminstatus;
    }

    public void setAdminstatus(String adminstatus) {
        this.adminstatus = adminstatus;
    }

    public String getAdminimage() {
        return adminimage;
    }

    public void setAdminimage(String adminimage) {
        this.adminimage = adminimage;
    }

    public String getAdminuid() {
        return adminuid;
    }

    public void setAdminuid(String adminuid) {
        this.adminuid = adminuid;
    }
}